function MileToYard(mile){
    const result = mile * 1760;
    return result;
}
console.log("Mile to Yard:", MileToYard(13), "yard")

function KilowattHourToKilocalorie(kwh){
    const result = kwh * 860;
    return result;
}
console.log("Kilo-watt hour to kilocalorie:", KilowattHourToKilocalorie(3), "kilocalorie")

function HoursToSecond(hours){
    const result  = hours * 3600;
    return result;
}
console.log("hours to second:", HoursToSecond(24), "second")

function CentimeterToMeter(centimeter){
    const result = centimeter / 100;
    return result;
}
console.log("Centimeter to meter:", CentimeterToMeter(20), "meter")

function InchToCentimeter(inch){
    const result = inch * 2.54;
    return result;
}
console.log("Inch to centimeter:", InchToCentimeter(24), "centimeter")

function PoundToKilogram(pound){
    const result = pound * 0.453;
    return result;
}
console.log("Pound to kilogram:", PoundToKilogram(24), "kilogram")

function YardToMeter(yard){
    const result = yard * 0.91;
    return result;
}
console.log("Yard to meter:", YardToMeter(23), "meter")